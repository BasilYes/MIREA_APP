package com.example.mirea_app.ui.main;

public class GameIconInfo {
    private String url;
    private String type;

    public GameIconInfo(String url, String type) {
        this.url = url;
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public String getType() {
        return type;
    }
}
